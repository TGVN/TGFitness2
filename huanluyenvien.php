			    var $items = $('.box_giaovien'); // Chọn tất cả các khóa học
			    var numItems = $items.length; // Đếm tổng số khóa học
			    var numPages = Math.ceil(numItems / itemsPerPage); // Tính tổng số trang

			    // Ẩn tất cả các khóa học, chỉ hiển thị các khóa học của trang đầu tiên
			    $items.hide().slice(0, itemsPerPage).show();

			    // Tạo các nút phân trang
			    $('#pagination').pagination({
			        items: numItems,
			        itemsOnPage: itemsPerPage,
			        cssStyle: 'light-theme',
			        prevText: '❮', // Tùy chỉnh nút "Prev"
			        nextText: '❯', // Tùy chỉnh nút "Next"

			        onPageClick: function (pageNumber) {
			            // Ẩn tất cả các khóa học và chỉ hiển thị các khóa học của trang được chọn
			            var start = (pageNumber - 1) * itemsPerPage;
			            var end = start + itemsPerPage;
			            $items.hide().slice(start, end).show();
